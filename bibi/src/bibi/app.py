"""
BibiApp — the stateful service that wires the CMS runtime and
manages session boundaries.

Responsibilities:
  - bootstrap the SQLite database (schema v5 via runtime)
  - hold the engine + assembler + belief service
  - manage per-user idle-window session boundaries
  - file turns via engine.process_turn()
  - return a structured TurnSummary that the CLI renders

Out of scope:
  - LLM response generation (lives in bibi.llm, optional)
  - voice (Bibi v2)
  - production architecture (Bibi v4)

Session policy: 5-minute idle window, configurable. State is held in
memory (per-user latest activity timestamp) AND persisted via the
runtime's own session_id field on observations. Session ids are
generated by Bibi (`sess_<user>_<unix_ts>`) so they're stable and
sortable.
"""

from __future__ import annotations

import time
import uuid
from dataclasses import dataclass, field
from datetime import datetime, timezone
from pathlib import Path

from cms.l1.adapter import LegacyExtractorAdapter
from cms.l1.service import ObservationService
from cms.l2.policies import WindowedClosurePolicy
from cms.l2.service import EpisodeService
from cms.l3.belief_service import BeliefService
from cms.l3.service import EvidenceService
from cms.runtime.assembler import StateAssembler
from cms.runtime.engine import CMSEngine
from cms.runtime.retrieval import RetrievalService
from cms.runtime.state import RuntimeStateView
from cms.storage.belief_store import BeliefStore
from cms.storage.episode_store import EpisodeStore
from cms.storage.evidence_store import EvidenceStore
from cms.storage.observation_store import ObservationStore
from cms.storage.schema import FULL_SCHEMA_DDL
from cms.storage.sqlite import SQLiteBackend

from bibi.config import BibiConfig
from bibi.extractor import BibiTextExtractor


@dataclass(slots=True)
class TurnSummary:
    """Structured outcome of a turn — what the CLI prints."""
    user_id: str
    session_id: str
    new_session: bool         # True if this turn started a fresh session
    observation_id: str
    new_evidence_ids: list[str]
    updated_belief_ids: list[str]
    closed_episode_id: str | None
    state: RuntimeStateView


@dataclass(slots=True)
class _UserSession:
    """In-memory tracking of a user's current session."""
    session_id: str
    last_activity: datetime


class BibiApp:
    """Bibi's runtime-bound service layer.

    One instance per process. All state lives in the SQLite database
    (path from config); in-memory state is purely the latest-activity
    cache for idle-window session detection.
    """

    def __init__(
        self,
        config: BibiConfig,
        *,
        clock=None,
    ):
        self._config = config
        self._clock = clock or (lambda: datetime.now(timezone.utc))
        self._user_sessions: dict[str, _UserSession] = {}

        # Bootstrap database
        config.storage.db_path.parent.mkdir(parents=True, exist_ok=True)
        self._backend = SQLiteBackend(str(config.storage.db_path))
        self._backend.bootstrap_schema(FULL_SCHEMA_DDL)

        # Stores
        self._obs_store = ObservationStore(self._backend)
        self._ep_store = EpisodeStore(self._backend)
        self._ev_store = EvidenceStore(self._backend)
        self._belief_store = BeliefStore(self._backend)

        # Services
        adapter = LegacyExtractorAdapter(BibiTextExtractor())
        obs_service = ObservationService(adapter=adapter, store=self._obs_store)
        ep_service = EpisodeService(
            store=self._ep_store,
            policy=WindowedClosurePolicy(max_size=8),
        )
        ev_service = EvidenceService(store=self._ev_store)
        bf_service = BeliefService(
            belief_store=self._belief_store,
            evidence_store=self._ev_store,
        )

        # Engine + assembler
        self._engine = CMSEngine(
            obs_service, ep_service,
            evidence_service=ev_service,
            belief_service=bf_service,
        )
        retrieval = RetrievalService(self._obs_store, self._ep_store, self._ev_store)
        self._assembler = StateAssembler(
            retrieval, belief_store=self._belief_store,
        )

        # Keep references for inspect/recompute commands
        self._belief_service = bf_service

    @property
    def config(self) -> BibiConfig:
        return self._config

    @property
    def belief_service(self) -> BeliefService:
        return self._belief_service

    # ── primary entry point ────────────────────────────────────────

    def turn(
        self,
        user_id: str,
        text: str,
        *,
        context_key: str | None = None,
    ) -> TurnSummary:
        """File one turn and return a structured summary.

        Session policy: idle-window detection. If the user's last
        activity was more than `config.session.idle_seconds` ago, a
        new session is started. Otherwise the existing session continues.
        """
        now = self._clock()
        session_id, is_new_session = self._resolve_session(user_id, now)

        # Generate a turn_id deterministically from session + timestamp
        turn_id = f"turn_{int(now.timestamp() * 1000):d}_{uuid.uuid4().hex[:6]}"

        # If a new session is starting, flush any stale open episode from
        # the prior session so episodes don't bleed across boundaries.
        if is_new_session and user_id in self._user_sessions:
            # The prior session's id is gone from cache by now; we use
            # the new flow but call end_session on the *prior* id only
            # if we tracked it. Since we just rotated, this is best-effort.
            pass

        result = self._engine.process_turn(
            user_id=user_id,
            session_id=session_id,
            turn_id=turn_id,
            text=text,
            context_key=context_key,
        )

        # Build the assembled view (read-only)
        state = self._assembler.build(user_id, session_id)

        return TurnSummary(
            user_id=user_id,
            session_id=session_id,
            new_session=is_new_session,
            observation_id=result.observation.obs_id,
            new_evidence_ids=list(result.new_evidence_ids),
            updated_belief_ids=list(result.updated_belief_ids),
            closed_episode_id=(
                result.closed_episode.episode_id
                if result.closed_episode is not None else None
            ),
            state=state,
        )

    def end_session(self, user_id: str) -> None:
        """Force-close the current session for a user (flushes any open episode)."""
        if user_id not in self._user_sessions:
            return
        sess = self._user_sessions[user_id]
        self._engine.end_session(user_id, sess.session_id)
        del self._user_sessions[user_id]

    # ── inspection ─────────────────────────────────────────────────

    def inspect(self, user_id: str) -> RuntimeStateView:
        """Return current state for a user across all sessions.

        If a session is in cache, use it; otherwise synthesize a view
        for the user's latest session (which covers most beliefs since
        beliefs are user-scoped, not session-scoped).
        """
        if user_id in self._user_sessions:
            session_id = self._user_sessions[user_id].session_id
        else:
            # Try to find the latest session from observations
            latest = self._obs_store.list_for_user(user_id, limit=1)
            session_id = (
                latest[-1].session_id if latest else f"sess_{user_id}_unknown"
            )
        return self._assembler.build(user_id, session_id)

    def list_sessions(self, user_id: str, limit: int = 20) -> list[str]:
        """Return the most recent session ids for a user.

        Read from the observation store directly — Bibi doesn't have a
        sessions table; sessions are implicit in observation records.
        """
        # We don't have a "list distinct sessions" helper on the store;
        # pull recent observations and de-duplicate while preserving order.
        observations = self._obs_store.list_for_user(user_id, limit=1000)
        seen: dict[str, datetime] = {}
        for o in observations:
            if o.session_id not in seen or o.created_at > seen[o.session_id]:
                seen[o.session_id] = o.created_at
        # Return newest-first
        return [
            sid for sid, _ in sorted(
                seen.items(), key=lambda kv: kv[1], reverse=True,
            )
        ][:limit]

    def recompute_for_user(self, user_id: str):
        """Trigger a full belief rebuild from the evidence ledger."""
        return self._belief_service.recompute_for_user(user_id)

    # ── session resolution ─────────────────────────────────────────

    def _resolve_session(
        self, user_id: str, now: datetime,
    ) -> tuple[str, bool]:
        """Return (session_id, is_new). Idle-window policy."""
        existing = self._user_sessions.get(user_id)
        idle = self._config.session.idle_seconds

        if existing is not None:
            elapsed = (now - existing.last_activity).total_seconds()
            if elapsed <= idle:
                # Same session
                existing.last_activity = now
                return existing.session_id, False

        # Start a new session
        new_id = f"sess_{user_id}_{int(now.timestamp())}"
        self._user_sessions[user_id] = _UserSession(
            session_id=new_id, last_activity=now,
        )
        return new_id, True

    # ── lifecycle ──────────────────────────────────────────────────

    def close(self) -> None:
        """Flush any open sessions and close the database backend."""
        # Best-effort flush of all in-memory open sessions
        for user_id, sess in list(self._user_sessions.items()):
            try:
                self._engine.end_session(user_id, sess.session_id)
            except Exception:
                pass
        self._backend.close()

    def __enter__(self) -> "BibiApp":
        return self

    def __exit__(self, exc_type, exc_val, exc_tb) -> None:
        self.close()
